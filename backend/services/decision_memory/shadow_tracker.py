"""shadow_tracker.py — Phase 3B.7 (stabilized, Phase 3B.9S)

Paper-trading valuation engine for ShadowPortfolio rows.

Two shadow types:
  STATIC_FROZEN — Holdings frozen at the moment a UserExecutionDecision was
                  recorded.  Simulates "what would have happened if the user
                  had followed the recommendation exactly."
  ACTIVE_MODEL  — Hypothetical 100%-compliant portfolio rebuilt each time the
                  optimizer runs for the same source portfolio.  Tracks the
                  live frontier of the AI's best guess.

Price source policy
-------------------
All price lookups use AgentCache (technical agent) — never live yfinance
calls.  This means paper valuations stay within the project's existing cache
TTL (15 min for technical data) and never block the daily scheduler with
network I/O.

Fallback chain (per holding):
  1. AgentCache current_price / price / close   (live)
  2. inception_price stored at shadow creation   (frozen)
  3. Skip holding entirely (warn + exclude)      (never zero)

NAV floor guard
---------------
If the total paper NAV collapses to < 1.0 despite inception_value > 0
(all prices temporarily missing), the engine falls back to:
  - Last non-zero ShadowPortfolioSnapshot.total_value
  - Otherwise inception_value itself

This prevents fake -100% inception returns while prices are unavailable.

Price-frozen holdings
---------------------
When a shadow is created and no AgentCache price is available for a symbol,
_resolve_shares_from_weights stores:
    shares = market_value_dollars, inception_price = 1.0, price_frozen = True

In _compute_paper_value, price_frozen=True holdings ALWAYS use inception_price
(1.0), regardless of what AgentCache later contains.  Without this guard,
market_value_dollars × real_price inflates NAV by 100-300×.

Backward-compat: for DB rows written before this patch, holdings with
inception_price == 1.0 and shares > 1000 are inferred as price_frozen.

Paper cash accounting (correctness fix, 2026-07-07)
----------------------------------------------------
Target weights intentionally sum to less than 100% — the optimizer's own
cash floor (OPTIMIZER_PHILOSOPHY.md §2 Priority 7 "Idle-Cash Efficiency";
policy_engine.py's min_cash_pct enforcement). _resolve_shares_from_weights
now returns (holdings, cash) instead of holdings alone: cash is the
unallocated residual, (100 - sum(target_weight)) / 100 * total_portfolio_
value. Every caller persists it to ShadowPortfolio.paper_cash_balance and
carries it into the next rebalance's NAV basis. Previously this residual
was computed nowhere and stored nowhere — deleted from NAV at every
rebalance, compounding multiplicatively. assert_nav_conserved() is the
regression gate: every rebalance/creation site asserts equity + cash ==
the NAV it was derived from, and raises NavInvariantError (loud failure,
never silent) if not. See docs/DECISION_LOG.md, "Paper Portfolio Cash-Leak
Fix," for the full analysis. Historical ShadowPortfolioSnapshot rows are
NOT regenerated by this fix — only future rebalances stop leaking.

create_recommendation_shadow(db, snapshot_id, workspace_id) → dict
  AI Evaluation M1 (P2): a THIRD use of shadow_type="STATIC_FROZEN", keyed to
  the recommendation itself rather than to a human decision — see docstring
  on the function below for how it differs from create_static_frozen_shadow.

Historical regeneration + replay validation (Accounting Correctness Milestone 2,
2026-07-07)
----------------------------------------------------------------------------
Milestone 1 fixed the shared replay functions so *future* rebalances stop
leaking cash, but explicitly did not touch already-persisted
ShadowPortfolioSnapshot rows or ShadowPortfolio.paper_cash_balance values
written before the fix (docs/DECISION_LOG.md, "Paper Portfolio Cash-Leak
Fix"). Milestone 2 adds the regeneration engine that re-derives those
derived rows through the now-correct engine, plus a reusable determinism
check any future simulation engine can reuse:

  verify_deterministic_replay(fn, *args, **kwargs) — calls a pure replay
      function twice and deep-compares the results within tolerance; raises
      ReplayNonDeterministicError (loud, never silent) on divergence.

  regenerate_static_shadow(db, shadow_id) — re-derives (holdings, cash) for
      one STATIC_FROZEN or recommendation-keyed shadow from its ORIGINAL
      allocation source (never re-normalized, never re-decided) and replays
      its snapshot history via the existing _rebuild_shadow_snapshots.

  regenerate_active_model_shadow(db, portfolio_id) — ACTIVE_MODEL holdings
      change at every optimizer rebalance, so its history cannot be replayed
      from one fixed holdings set (this is why repair_shadow_portfolios has
      always skipped it — see that function's docstring). This function
      walks every RecommendationSnapshot from the shadow's immutable
      inception_date forward, exactly mirroring create_active_model_shadow's
      rebalance mechanic and ideal_series.py's day-by-day replay shape, and
      persists one ShadowPortfolioSnapshot per day.

  regenerate_portfolio_paper_history(db, portfolio_id, workspace_id) —
      orchestrates both of the above for one portfolio, then leaves
      AttributionMetric refresh to the caller (compute_portfolio_attribution
      is idempotent per day and reads shadow snapshots fresh — no separate
      regeneration step is meaningful for it, since it is a rolling
      trailing-window figure keyed to "today," not a historical series).

None of this touches RecommendationSnapshot, PortfolioSnapshot, PortfolioItem,
Transaction, or UserExecutionDecision — only ShadowPortfolio /
ShadowPortfolioSnapshot rows are rewritten, and shadow.inception_date /
shadow.inception_value are never touched (identity-preserving, values-only).

Public API
----------
value_shadow_portfolio(db, shadow_id) → dict
create_static_frozen_shadow(db, execution_decision_id, workspace_id) → dict
create_active_model_shadow(db, portfolio_id, snapshot_id, workspace_id) → dict
create_recommendation_shadow(db, snapshot_id, workspace_id) → dict
value_all_active_shadows(db, workspace_id) → list[dict]
repair_shadow_portfolios(db, workspace_id) → list[dict]
reset_active_model_inception(db, workspace_id) → list[dict]
verify_deterministic_replay(replay_fn, *args, **kwargs) → dict
regenerate_static_shadow(db, shadow_id) → dict
regenerate_active_model_shadow(db, portfolio_id) → dict
regenerate_portfolio_paper_history(db, portfolio_id, workspace_id) → dict
"""
